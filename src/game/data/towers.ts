export const getTowerDefinition = (towerId: string): TowerDefinition => {
  const tower = towerDefinitions.find((definition) => definition.id === towerId);

  if (!tower) {
    throw new Error(`Tower definition not found: ${towerId}`);
  }

  return tower;
};

export const getTowerDefinitionsForClass = (classId: string): readonly TowerDefinition[] =>
  towerDefinitions.filter((definition) => definition.originClassId === classId);
